WITH EMOJIS AS (
  SELECT
    content
  FROM 
    `${PROJECT_ID}.${DATASET_ID}.${TABLE_ID}` 
    ,UNNEST(REGEXP_EXTRACT_ALL(content, r'[\\U0001F600-\\U0001F64F]|[\\U0001F300-\\U0001F5FF]|[\\U0001F680-\\U0001F6FF]|[\\U0001F1E0-\\U0001F1FF]|[\\U0001F90D-\\U0001F9FF]')) AS emoji
)
SELECT
  content
  ,COUNT(1) AS emoji_count
FROM EMOJIS
GROUP BY 1
ORDER BY emoji_count DESC
LIMIT 10